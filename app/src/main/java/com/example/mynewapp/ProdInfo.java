package com.example.mynewapp;


public class ProdInfo {
    String product_name;
    long product_price;
    String product_specs;

    public ProdInfo() {
    }

    public String getProduct_specs() {
        return product_specs;
    }

    public long getProduct_price() {
        return product_price;
    }

    public String getProduct_name() {
        return product_name;
    }

}
